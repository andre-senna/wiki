#!/bin/bash

PROJECT_NAME=$1
SERVICE_NAME=$2
SERVICE_PORT=$3

TEMPLATE_DIR=template
SRC_DIR=$PROJECT_NAME/src
BIN_DIR=$PROJECT_NAME/bin
PROTO_DIR=$SRC_DIR/service_spec
PROTO_FILE=$PROTO_DIR/$PROJECT_NAME.proto
SERVER_CC_FILE=$SRC_DIR/server.cc
CLIENT_CC_FILE=$SRC_DIR/client.cc
MAKEFILE_FILE=$SRC_DIR/Makefile
SERVICE_JSON_FILE=$PROJECT_NAME/service.json
DAEMON_CONFIG_DIRNAME=config
DAEMON_CONFIG_FILENAME=snetd.config.json
DAEMON_CONFIG_DIR=$PROJECT_NAME/$DAEMON_CONFIG_DIRNAME
DAEMON_CONFIG_FILE=$DAEMON_CONFIG_DIR/$DAEMON_CONFIG_FILENAME
START_SERVICE_SCRIPT_FILE=$PROJECT_NAME/publishAndStartService.sh

TEMPLATE_PROTO=$TEMPLATE_DIR/proto.txt
TEMPLATE_MAKEFILE=$TEMPLATE_DIR/makefile.txt
TEMPLATE_SERVER=$TEMPLATE_DIR/server.txt
TEMPLATE_CLIENT=$TEMPLATE_DIR/client.txt
TEMPLATE_SERVICE_JSON=$TEMPLATE_DIR/serviceJson.txt
TEMPLATE_DAEMON_CONFIG_FILE=$TEMPLATE_DIR/daemonConfigFile.txt
TEMPLATE_START_SERVICE_SCRIPT=$TEMPLATE_DIR/publishAndStartService.txt

PROJECT_TAG=__PROJECT__
SERVICE_PORT_TAG=__SERVICE_PORT__
SERVICE_NAME_TAG=__SERVICE_NAME__
DAEMON_CONFIG_FILE_TAG=__DAEMON_CONFIG_FILE__

if [ -d "$PROJECT_NAME" ] || [ -f $PROJECT_NAME ]; then
    echo "ERROR: $PROJECT_NAME already exists"
    exit 1
fi

mkdir -p $PROTO_DIR
mkdir -p $SRC_DIR
mkdir -p $BIN_DIR
mkdir -p $DAEMON_CONFIG_DIR

cat $TEMPLATE_PROTO | sed "s/$PROJECT_TAG/$PROJECT_NAME/g" > $PROTO_FILE
cat $TEMPLATE_MAKEFILE | sed "s/$PROJECT_TAG/$PROJECT_NAME/g" > $MAKEFILE_FILE
cat $TEMPLATE_SERVER | sed "s/$PROJECT_TAG/$PROJECT_NAME/g" | sed "s/$SERVICE_PORT_TAG/$SERVICE_PORT/g" > $SERVER_CC_FILE
cat $TEMPLATE_CLIENT | sed "s/$PROJECT_TAG/$PROJECT_NAME/g" | sed "s/$SERVICE_PORT_TAG/$SERVICE_PORT/g" > $CLIENT_CC_FILE
cat $TEMPLATE_SERVICE_JSON | sed "s/$PROJECT_TAG/$PROJECT_NAME/g" | sed "s/$SERVICE_PORT_TAG/$SERVICE_PORT/g" | sed "s/$SERVICE_NAME_TAG/$SERVICE_NAME/g" > $SERVICE_JSON_FILE
cat $TEMPLATE_DAEMON_CONFIG_FILE > $DAEMON_CONFIG_FILE
cat $TEMPLATE_START_SERVICE_SCRIPT | sed "s/$DAEMON_CONFIG_FILE_TAG/$DAEMON_CONFIG_DIRNAME\/$DAEMON_CONFIG_FILENAME/g" > $START_SERVICE_SCRIPT_FILE
chmod o+x $START_SERVICE_SCRIPT_FILE
